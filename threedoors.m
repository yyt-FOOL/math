    x=randi([1,3]);
    y=randi([1,3]);
    if x==y
        a=a+1;
        b=b+0;%如果一开始就选中了汽车，那么只有不改变注意才会获得汽车
    else 
        a=a+0;
        b=b+1;%如果一开始选中了山羊，那么只有改变主意才可以获得山羊
    end
end
