x4=[8 6 9 16 15 17 8 10 4 16 7 12 6 4 4 14 6 8 13 11]';
y=[79.3 200.1 163.2 200.1 146.0 177.7 30.9 291.9 160.0 339.4 159.6 86.3 237.5 107.2 ...
    155.0 201.4 100.2 135.8 223.3 195.0]';
X=[ones(size(x1)) x1 x2 x3 x4];
