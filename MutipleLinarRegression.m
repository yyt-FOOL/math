%% 逐步线性回归
%数据输入
x1=[7 1 11 11 7 11 3 1 2 21 1 11 10]';
x2=[26 29 56 31 52 55 71 31 54 47 40 66 68]';
x3=[6 15 8 8 6 9 17 22 18 4 23 9 8]';
x4=[60 52 20 47 33 22 6 44 22 26 34 12 12]';
y=[78.5 74.3 104.3 87.6 95.9 109.2 102.7 72.5 93.1 115.9 83.8 113.3 109.4]';
x=[x1 x2 x3 x3];
%调用逐步回归函数
stepwise(x,y)
%选用x1、x2进行多元线性回归
x_1=[ones(size(x1)) x1 x2];
[b,bint,r,rint,stats]=regress(y,x_1)
%绘制残差图
figure(2)
rcoplot(r,rint)
%绘制平面图
[X1,X2]=meshgrid(x1,x2);%创建网格点坐标矩阵
z=b(1)+b(2)*X1+b(3)*X2;
figure(3)
surf(x1,x2,z)
xlabel('x1')
ylabel('x2')
zlabel('z')
title(['z=' num2str(b(1)) '+' num2str(b(2)) '*x1+' num2str(b(3)) '*x2'])
