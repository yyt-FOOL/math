clc;clear
a=(0:0.001:0.05);
f=[-0.05,-0.27,-0.19,-0.185,-0.185];
o=zeros(4,1);
A=[o,diag([0.025,0.015,0.055,0.026])];%不等式约束条件矩阵
Aeq=[1,1.01,1.02,1.045,1.065];%等式约束条件矩阵
beq=1;%等式约束条件系数
b1=zeros(5,1);%隐藏约束条件的系数矩阵
Q=zeros(1,length(a));%用于接收最优解值
X=[];%用于接收最优解矩阵
for i=1:length(a)
    b=a(i)*ones(4,1);
    [x,y]=linprog(f,A,b,Aeq,beq,b1);
    Q(i)=-y;
    X=[X;x'];
end
disp(X)
plot(a,Q,'*r');
xlabel('风险率');
ylabel('最大收益');